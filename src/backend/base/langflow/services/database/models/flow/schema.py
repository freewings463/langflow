"""
模块名称：`Flow` 标签枚举

本模块定义流程标签的枚举集合。
主要功能包括：统一标签值以便筛选与分类。

关键组件：`Tags`
设计背景：避免标签字符串散落导致不一致。
使用场景：流程分类、筛选与 UI 展示。
注意事项：新增标签需同步前端与文档。
"""

from enum import Enum


class Tags(str, Enum):
    """流程标签枚举。

    契约：
    - 取值：`CHATBOTS`/`AGENTS`。
    - 用途：限定流程标签范围。
    - 失败语义：非法取值由枚举校验抛出。

    决策：采用枚举限制标签集合。
    问题：自由字符串易产生拼写与语义漂移。
    方案：集中定义标签枚举。
    代价：新增标签需要代码变更。
    重评：当标签需动态扩展时改为配置驱动。
    """
    CHATBOTS = "chatbots"
    AGENTS = "agents"
